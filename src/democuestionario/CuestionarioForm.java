    private class ButtonHandler implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            //seleccion de respuesta
            for (Enumeration<AbstractButton> buttons = buttonGroup1.getElements(); buttons.hasMoreElements();) {
                AbstractButton button = buttons.nextElement();

                if (button.isSelected()) {
                    //System.out.println(button.getText());
                    if(respuesta == button.getText()){
                        JOptionPane.showMessageDialog(null,"FELICITACIONES RESPUESTA CORRECTA!","Respuesta correcta",JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null,"RESPUESTA INCORRECTA INTENTA DE NUEVO","Respuesta incorrecta",JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
        
    }

